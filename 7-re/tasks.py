"""
Задание №1.
Напишите регулярное выражение, которое найдёт все 4-значные года в этом тексте 
(предполагается, что все года лежат между 1000 и 2999).
Подсказки:
Про год: 3150 принимать не нужно, выбираем не все цифры.
"""

import re
from decimal import *

text = """
В 1910 году Генри Норрис Расселл изучал звёзды.
В 1926 году Фаулер опубликовал статью.
Юнона пролетела мимо Европы в 2022 году.
А в 2031 году ожидается прибытие JUICE.
"""

result = re.findall(r'\b[12]\d{3}\b', text)
print(result)

