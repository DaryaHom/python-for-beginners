"""
Задание №3. 
Валидация российского ИНН физического лица (12 цифр)
ИНН физлица в РФ — ровно 12 цифр, без букв и пробелов.
Создайте шаблон регулярным выражением, который выдает True/False.
True - ИНН верно введен, False - некорректный ввод.
"""
def is_inn_correct(inn: str) -> bool:
    return re.fullmatch(r'\d{12}', inn) is not None

"""
Задание №4.
Тестирование класса
У счёта есть атрибут balance (баланс).
При создании счёт должен иметь баланс 0.0.

Метод deposit(amount):
1. Принимает сумму amount (число).
2. Увеличивает баланс на эту сумму.

Метод withdraw(amount):
1. Принимает сумму amount.
2. Уменьшает баланс на эту сумму.

Не нужно обрабатывать ошибки (отрицательные суммы, снятие сверх баланса) — тесты этого не проверяют.

Напишите тесты, которые проверяют:
1. Начальный баланс — при создании счёта BankAccount() его balance должен быть 0.0.
2. Пополнение счёта — после deposit(100) баланс становится 100.0.
3. Снятие денег — если сначала положить 200, а потом снять 50, баланс должен стать 150.0.
4. Независимость счетов — если создать два счёта, операции над одним не влияют на другой.
"""

class BankAccount:
    def __init__(self):
        self.__balance = Decimal('0.0')

    @property
    def balance(self): 
        return self.__balance

    def deposit(self, amount: Decimal):
        # Ошибки не обрабатываются по условию задачи
        self.__balance += amount

    def withdraw(self, amount: Decimal):
        # Ошибки не обрабатываются по условию задачи
        self.__balance -= amount

"""
Задание №5. Доп. задание
Валидация имени пользователя (login)
Условие:
Только латинские буквы, цифры, точки и подчёркивания
Длина: от 3 до 20 символов
Не может начинаться или заканчиваться точкой или подчёркиванием
Не может содержать два символа подряд вроде .. или __
"""
def is_login_correct(login: str) -> bool:
    return re.fullmatch(r'(?!.*[._]{2})[A-Za-z]{1}[A-Za-z._]{1,18}[A-Za-z]{1}', login) is not None

"""
Задание #6. 
Условие:
Начинается с #
Далее 3 или 6 шестнадцатеричных символов (0-9, a-f, A-F)
Пример: #00ffff , #5d8aa8
"""
def is_hex_color_correct(color: str) -> bool:
    return re.fullmatch(r'#[A-Fa-f0-9]{3,6}', color) is not None
